#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	if (!f)
		return ;
	if (!s)
		return ;
	i = 0;
	while (*s != '\0')
	{
		f(i++, s++);
	}
}
